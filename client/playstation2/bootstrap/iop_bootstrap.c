/* client/playstation2/bootstrap/iop_bootstrap.c
 *
 * Loads the small IOP-side drivers needed for PS2 Ethernet.
 *
 * The PS2 has two CPUs:
 *   - EE: runs this loader and the uploaded program.
 *   - IOP: owns DEV9/SMAP, the hardware path to the Ethernet adapter.
 *
 * This file asks the IOP's resident LOADFILE service to load our IRXs:
 *   1. kosdev9.irx: prepares DEV9 and exposes ReadEEPROM.
 *   2. smap.irx: runs the Ethernet driver and RPC server.
 *
 * It uses the normal SIFRPC/LOADFILE path instead of patching IOP exception
 * vectors, which proved less reliable on real hardware.
 */

